CREATE TABLE SUPPLIERS (
ID NUMBER,
CONSTRAINT SUPPLIER_ID PRIMARY KEY (ID),
CompanyName VARCHAR2(200) NOT NULL,
ContactName VARCHAR2(100),
ContactTitle VARCHAR2(100),
Address VARCHAR2(200),
City VARCHAR2(100),
Region VARCHAR2(100),
PostalCode NUMBER(10),
Country VARCHAR2(100),
Phone VARCHAR2(20),
Fax VARCHAR2(20),
HomePage VARCHAR2(100)
);